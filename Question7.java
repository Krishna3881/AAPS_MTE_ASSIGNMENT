// QUESTION 7
// Explain the sliding window technique and its use in string problems.
// ANSWER
// The sliding window technique involves maintaining a window (substring) over a range and
// moving it forward to process or analyze data efficiently. It is widely used in problems such 
// as:
// • Longest substring without repeating characters
// • Minimum window substring
// • Anagram finding in a string
// Advantages:
// • Reduces time complexity from O(n²) to O(n)
// • Ideal for contiguous segment problems
